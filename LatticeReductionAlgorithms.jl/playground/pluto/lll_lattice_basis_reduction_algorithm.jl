@testset "LLL_reduce" begin
	@testset "Example 2.3.9 for δ = 0.75" begin
		B = [
			9 8 3
			2 6 2
			7 1 6
		]
		δ = 0.75
		g = LLL_reduce(B, δ)
		@test g.B == [
			 -1  2   3
			  4  6  -2
			 -6  0  -5
		]
	end
	
	@testset "Example 2.3.9 for δ = 0.99" begin
		B = [
			9 8 3
			2 6 2
			7 1 6
		]
		δ = 0.99
		g = LLL_reduce(B, δ)
		@test g.B == [
			6 3  2
			0 -2 6
			1 -5 0
		]
	end
	
	@testset "Example 2.3.10 for δ = 0.9999999" begin
		B = [
			-2  3  2  8
	 		 7 -2 -8 -9
			 7  6 -9  6
			-5 -1 -7 -4
		]
		δ = 0.9999999
		g = LLL_reduce(B, δ)
		@test g.B == [
			 2   2  -2   3
			 3   0   2  -2
			 1  -2   3   6
			 1  -4  -3  -1
		]

		α = 4 / (4δ - 1)
		n = size(B, 2)
		volL = abs(det(B))
		@test norm(g.B[:, 1]) ≤ α ^ ((n - 1)/4) * volL ^ (1/n)
		@test prod(norm(g.B[:, i]) for i in 1:n) ≤ α ^ (n * (n - 1)/4) * volL
	end
end

